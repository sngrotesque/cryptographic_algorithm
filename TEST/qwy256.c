void qwy256_init_ctx(qwy256_ctx *ctx, uint8_t *key)
{
    uint8_t round, index, keyBuffer[QWY256_BLOCKLEN];
    memcpy(keyBuffer, key, QWY256_BLOCKLEN);
    for(round = 0; round < QWY256_ROUNDS; ++round) {
        memcpy(ctx->RK[round], keyBuffer, QWY256_BLOCKLEN);
        for(index = 0; index < QWY256_BLOCKLEN; ++index) {
            keyBuffer[index] = QWY256_RK(keyBuffer[index], ctx->IV[index]);
            keyBuffer[index] = keyBuffer[index] ^ (round + index);
            swap_uint8(&keyBuffer[index], &ctx->IV[index]);
            keyBuffer[index] = keyBuffer[index] - (((round + index) << 1) / 3);
            swap_uint8(&keyBuffer[index], &ctx->IV[index]);
            keyBuffer[index] = keyBuffer[index] ^ (round + index);
        }
    }
}

void ColumnMix(uint8_t r, qwy_state_t *buffer)
{
    uint8_t x, y, first, last;
    for(x = 0; x < r + 4 ; ++x) {
        for(y = 0; y < QWY256_Maximum - 1; ++y) {
            first = y, last = QWY256_RANGE(y);
            swap_uint8(&(*buffer)[r][first], &(*buffer)[r][last]);
        }
    }
}

void qwy256_cipher(qwy_state_t *state, qwy_state_t *roundKey)
{
    uint8_t x, y;
    for(x = 0; x < QWY256_Minimum; ++x) {
        for(y = 0; y < QWY256_Maximum; ++y) {
            (*state)[x][y] = GetSBOX_VALUE((*state)[x][y]);
            (*state)[x][y] = QWY256_E((*state)[x][y], (*roundKey)[x][y]);
        }
    }
}

