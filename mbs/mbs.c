snVoid MBS_init_ctx(MBS_ctx *ctx, snByte *key)
{
    register sn_u32 r;
    register sn_u32 i;
    register snByte buf;

    for(r = 0; r < MBS_NR; ++r) {
        memcpy(ctx->rk[r], key, MBS_BLOCKLEN);

        for(i = 0; i < MBS_BLOCKLEN; ++i) {
            key[i] ^= ctx->iv[i];
            key[i] ^= 0xc9;
            key[i] = bitSwitch(key[i]);
            key[i] = MBS_GET_SBOX(key[i]);
            key[i] = bitSwitch(key[i]);

            key[i] ^= key[(i + 7) % MBS_BLOCKLEN];
            key[i] ^= key[(i + 13) % MBS_BLOCKLEN];
            key[i] ^= key[(i + 24) % MBS_BLOCKLEN];
            key[i] ^= key[(i + 33) % MBS_BLOCKLEN];
            key[i] ^= key[(i + 41) % MBS_BLOCKLEN];
            key[i] ^= key[(i + 47) % MBS_BLOCKLEN];
            key[i] ^= key[(i + 54) % MBS_BLOCKLEN];
            key[i] ^= key[(i + 62) % MBS_BLOCKLEN];
        }

        for(i = 0; i < MBS_BLOCKLEN; ++i)
            ctx->iv[i] ^= key[i];

        MBS_BlockTransformat((mbsState_t *)key);
    }
}

snVoid MBS_CBC_Encrypt(MBS_ctx *ctx, snByte *buf, snSize_t size)
{
    static mbsState_t *state;
    static mbsState_t *stateIv;
    static snSize_t r;
    static snSize_t i;
    static snSize_t x;
    static snSize_t y;
    static snSize_t addr;

    for(r = 0; r < MBS_NR; ++r) {
        for(i = 0; i < size / MBS_BLOCKLEN; ++i) {
            MBS_BlockTransformat((mbsState_t *)(buf + addr));
            MBS_Cipher((mbsState_t *)(buf + addr),
                       (mbsState_t *)(ctx->rk[r]),
                       (mbsState_t *)MBS_sbox);
            addr = ((i + 1) * MBS_BLOCKLEN);
        }
    }
}
