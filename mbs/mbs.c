#define MBS_GET_SBOX(x) (MBS_sbox[(x)])

static snVoid MBS_BlockTransformat(mbsState_t *state)
{
    register sn_u32 x;
    register sn_u32 y;
    register sn_u32 r;
    register snByte buf;

    // 行移位
    for(r = 0; r < MBS_NK; ++r)
        for(x = r; x < MBS_NK; ++x)
            for(y = 0; y < MBS_NK - 1; ++y) {
                buf = (*state)[x][y];
                (*state)[x][y] = (*state)[x][y + 1];
                (*state)[x][y + 1] = buf;
            }

    // 列混合
    for(r = 0; r < MBS_NK; ++r)
        for(x = r; x < MBS_NK; ++x)
            for(y = 0; y < MBS_NK - 1; ++y) {
                buf = (*state)[y][x];
                (*state)[y][x] = (*state)[y + 1][x];
                (*state)[y + 1][x] = buf;
            }

    // 修补没有被移位的数据
    buf = (*state)[0][0];
    (*state)[0][0] = (*state)[MBS_NK - 1][MBS_NK - 1];
    (*state)[MBS_NK - 1][MBS_NK - 1] = buf;
}

snVoid MBS_Cipher(mbsState_t *state, mbsState_t *key, mbsState_t *sbox)
{
    static sn_u32 x;
    static sn_u32 y;

    for(x = 0; x < MBS_NK; ++x) {
        for(y = 0; y < MBS_NK; ++y) {
            (*state)[x][y] = MBS_GET_SBOX((*state)[x][y]);
            (*state)[x][y] = MBS_E((*state)[x][y], (*key)[x][y], (*sbox)[x][y]);
            (*state)[x][y] = MBS_GET_SBOX((*state)[x][y]);
        }
    }
}

